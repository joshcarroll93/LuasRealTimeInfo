package project.josh.carroll.luasrealtimeinfo.Data;

public class LuasStop {

    private String abbreviation;
    private String cycleAndRide;
    private String parkAndRide;
    private String latitude;
    private String longitude;
    private String pronunciation;

    public String getAbbreviation() {
        return abbreviation;
    }

    public void setAbbreviation(String abbreviation) {
        this.abbreviation = abbreviation;
    }

    public String getCycleAndRide() {
        return cycleAndRide;
    }

    public void setCycleAndRide(String cycleAndRide) {
        this.cycleAndRide = cycleAndRide;
    }

    public String getParkAndRide() {
        return parkAndRide;
    }

    public void setParkAndRide(String parkAndRide) {
        this.parkAndRide = parkAndRide;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getPronunciation() {
        return pronunciation;
    }

    public void setPronunciation(String pronunciation) {
        this.pronunciation = pronunciation;
    }
}
